package com.example.codesquad.week1.misson2.mission2_3;

public class Drink {
    private String coffee;
    private int price;

    public  Drink(String coffee, int price) {
        this.coffee = coffee;
        this.price = price;
    }

    public String getCoffee() {
        return coffee;
    }

    public int getPrice() {
        return price;
    }
}
